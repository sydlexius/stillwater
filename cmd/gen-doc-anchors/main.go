// Command gen-doc-anchors walks docs/site/src/**/*.md (excluding the
// reference/ subdirectory and any existing _doc-anchors.txt) and emits a
// sorted companion file _doc-anchors.txt at two mirror locations:
//
//   - docs/site/src/reference/_doc-anchors.txt  (canonical; MkDocs vendor copy)
//   - web/components/_doc-anchors.txt            (embedded by TestContextHelpAnchors)
//
// Each line in the output has the form:
//
//	<docs-path-without-.md>#<slug>
//
// where docs-path is relative to docs/site/src/ (e.g. "core-concepts/rules")
// and slug is the MkDocs / python-markdown default toc extension slug for
// that heading.
//
// Usage:
//
//	go run ./cmd/gen-doc-anchors              # rewrite both files in place
//	go run ./cmd/gen-doc-anchors -check       # exit non-zero if regen needed
//	go run ./cmd/gen-doc-anchors -output DIR  # write canonical copy to DIR/_doc-anchors.txt
package main

import (
	"bufio"
	"flag"
	"fmt"
	"os"
	"path/filepath"
	"regexp"
	"sort"
	"strings"
	"unicode"

	"golang.org/x/text/unicode/norm"

	"github.com/sydlexius/stillwater/internal/filesystem"
)

const (
	// defaultDocsRoot is the source tree for MkDocs content. Paths are
	// relative to the current working directory (repo root when invoked via
	// `make generate-docs` or `go run ./cmd/gen-doc-anchors`).
	defaultDocsRoot = "docs/site/src"

	// defaultCanonicalPath is the canonical anchors output path.
	defaultCanonicalPath = "docs/site/src/reference/_doc-anchors.txt"

	// componentsMirrorPath is the in-package copy consumed by the test embed.
	componentsMirrorPath = "web/components/_doc-anchors.txt"

	// fileHeader is prepended to every emitted anchors file so contributors
	// know not to edit it manually.
	fileHeader = "# Generated by gen-doc-anchors. DO NOT EDIT.\n"
)

// skipPaths lists docs-root-relative paths to exclude from the walk. The
// reference/ subdirectory already has its own codegen toolchain
// (gen-settings-reference, gen-rules-catalogue); including it would emit
// anchors for the generated settings page, which the test routes through
// _settings-anchors.txt instead. The anchors files themselves must also be
// excluded to avoid an infinite self-reference.
var skipPaths = []string{
	"reference",
}

func main() {
	var (
		checkOnly bool
		outDir    string
	)
	flag.BoolVar(&checkOnly, "check", false, "exit non-zero if the anchor files need to be regenerated")
	flag.StringVar(&outDir, "output", "", "write canonical copy to <dir>/_doc-anchors.txt instead of the default")
	flag.Parse()

	canonicalPath := defaultCanonicalPath
	if outDir != "" {
		canonicalPath = filepath.Join(outDir, "_doc-anchors.txt")
	}

	if err := run(defaultDocsRoot, canonicalPath, checkOnly); err != nil {
		fmt.Fprintln(os.Stderr, "gen-doc-anchors:", err)
		os.Exit(1)
	}
}

func run(docsRoot, canonicalPath string, checkOnly bool) error {
	anchors, err := collectAnchors(docsRoot)
	if err != nil {
		return fmt.Errorf("collect anchors: %w", err)
	}

	// Write (or check) both paths: canonical under docs/ and the components
	// mirror. Only write the mirror when the canonical path is the default;
	// if -output redirected us elsewhere we leave the mirror alone.
	paths := []string{canonicalPath}
	if filepath.Clean(canonicalPath) == filepath.Clean(defaultCanonicalPath) {
		paths = append(paths, componentsMirrorPath)
	}

	for _, p := range paths {
		if err := writeOrCheck(p, anchors, checkOnly); err != nil {
			return err
		}
	}
	return nil
}

// collectAnchors walks docsRoot and returns a sorted slice of
// "<path>#<slug>" strings for every heading in every .md file,
// excluding the reference/ subdirectory.
func collectAnchors(docsRoot string) ([]string, error) {
	var anchors []string

	err := filepath.Walk(docsRoot, func(path string, info os.FileInfo, err error) error {
		if err != nil {
			return err
		}

		// Compute the path relative to docsRoot for skip checks and output.
		rel, relErr := filepath.Rel(docsRoot, path)
		if relErr != nil {
			rel = path
		}
		rel = filepath.ToSlash(rel)

		// Skip excluded directories.
		if info.IsDir() {
			for _, skip := range skipPaths {
				if rel == skip || strings.HasPrefix(rel, skip+"/") {
					return filepath.SkipDir
				}
			}
			return nil
		}

		// Only process Markdown files. The reference/ skip directive above
		// already drops both anchors files; this check additionally filters
		// any non-.md file (assets, JSON, etc.) that may live alongside docs.
		if !strings.HasSuffix(path, ".md") {
			return nil
		}

		// Compute the docs-path prefix (file path without .md extension).
		docPath := strings.TrimSuffix(rel, ".md")

		fileAnchors, err := headingsToAnchors(path, docPath)
		if err != nil {
			return fmt.Errorf("%s: %w", path, err)
		}
		anchors = append(anchors, fileAnchors...)
		return nil
	})
	if err != nil {
		return nil, err
	}

	sort.Strings(anchors)
	return anchors, nil
}

// headingsToAnchors reads a Markdown file at path and returns a slice of
// "<docPath>#<slug>" for every heading, applying duplicate-slug de-duplication
// consistent with python-markdown's toc extension (MkDocs default).
func headingsToAnchors(path, docPath string) ([]string, error) {
	f, err := os.Open(path) //nolint:gosec // G304: developer CLI, path comes from filepath.Walk in a known safe tree
	if err != nil {
		return nil, err
	}
	defer f.Close() //nolint:errcheck // deferred close on read-only file; error not actionable

	var anchors []string
	seen := make(map[string]int)
	inFence := false

	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		line := scanner.Text()

		// Track fenced code blocks so we don't mistake a code comment
		// starting with # for a Markdown heading.
		if strings.HasPrefix(line, "```") || strings.HasPrefix(line, "~~~") {
			inFence = !inFence
			continue
		}
		if inFence {
			continue
		}

		// Extract the heading text (strip leading # characters and space).
		if !strings.HasPrefix(line, "#") {
			continue
		}
		trimmed := strings.TrimLeft(line, "#")
		if len(trimmed) == 0 || trimmed[0] != ' ' {
			// Not a heading (e.g. "#anchor" without trailing space).
			continue
		}
		headingText := strings.TrimSpace(trimmed)
		if headingText == "" {
			continue
		}

		slug := slugify(headingText)

		// De-duplicate: second occurrence of the same slug gets a -1 suffix,
		// third gets -2, matching python-markdown toc behavior.
		count := seen[slug]
		seen[slug]++
		if count > 0 {
			slug = fmt.Sprintf("%s-%d", slug, count)
		}

		anchors = append(anchors, fmt.Sprintf("%s#%s", docPath, slug))
	}
	if err := scanner.Err(); err != nil {
		return nil, err
	}
	return anchors, nil
}

// nonAlphanumDash matches any character that is not an ASCII alphanumeric, a
// space, an underscore, or a hyphen, after NFKD normalization. These are
// stripped in the python-markdown default slugify (\w preserves underscore).
var nonAlphanumDash = regexp.MustCompile(`[^\w\s-]`)

// collapseSpace matches runs of whitespace or hyphens that should become a
// single hyphen. Underscores are deliberately NOT collapsed: python-markdown
// preserves them, so a heading like "snake_case" yields slug "snake_case".
var collapseSpace = regexp.MustCompile(`[\s-]+`)

// slugify converts a Markdown heading string to the slug produced by the
// python-markdown toc extension with the MkDocs default configuration
// (toc: permalink: true, no custom slugify callback).
//
// Algorithm:
//  1. NFKD-normalize and drop non-ASCII runes (strips accents).
//  2. Lowercase.
//  3. Strip anything that is not alphanumeric, space, hyphen, or underscore.
//  4. Collapse runs of whitespace and hyphens to a single hyphen (underscores
//     are preserved per python-markdown's `[-\s]+` collapse pattern).
//  5. Strip leading/trailing hyphens.
func slugify(heading string) string {
	// Step 1: NFKD normalize and strip non-ASCII.
	normalized := norm.NFKD.String(heading)
	var ascii strings.Builder
	for _, r := range normalized {
		if r <= unicode.MaxASCII {
			ascii.WriteRune(r)
		}
	}
	s := ascii.String()

	// Step 2: lowercase.
	s = strings.ToLower(s)

	// Step 3: remove characters not in [a-z0-9 _-].
	s = nonAlphanumDash.ReplaceAllString(s, "")

	// Step 4: collapse whitespace / underscores to a single hyphen.
	s = collapseSpace.ReplaceAllString(s, "-")

	// Step 5: strip leading/trailing hyphens.
	s = strings.Trim(s, "-")

	return s
}

// writeOrCheck writes (or verifies) the anchors file at dest.
// When checkOnly is true it exits non-zero if the file is stale.
func writeOrCheck(dest string, anchors []string, checkOnly bool) error {
	body := fileHeader + strings.Join(anchors, "\n") + "\n"

	existing, err := os.ReadFile(dest) //nolint:gosec // G304: developer CLI, path is from a known constant
	if err != nil && !os.IsNotExist(err) {
		return fmt.Errorf("read %s: %w", dest, err)
	}

	if string(existing) == body {
		return nil
	}

	if checkOnly {
		return fmt.Errorf("%s is stale; run `make generate-docs` to regenerate", filepath.ToSlash(dest))
	}

	// Atomic write (tmp/bak/rename) so an interrupted run can never leave a
	// partial anchors file on disk. WriteFileAtomic creates parent dirs as
	// needed, so no separate MkdirAll is required.
	return filesystem.WriteFileAtomic(dest, []byte(body), 0o644)
}
