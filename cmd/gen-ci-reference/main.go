// Command gen-ci-reference reads .github/workflows/ci.yml and generates a
// Markdown reference page documenting the CI job dependency graph (as a Mermaid
// flowchart), the test matrix shard configuration, and the aggregator pattern
// used to expose a single stable "Test" check to branch protection.
//
// The output is written to docs/_generated/ci-reference.md and included by
// docs/site/src/contributing/ci-reference.md via a pymdownx.snippets --8<--
// directive so the documented CI structure stays in sync with the workflow file
// and cannot drift.
//
// Usage:
//
//	go run ./cmd/gen-ci-reference              # rewrite the file in place
//	go run ./cmd/gen-ci-reference -check       # exit non-zero if regen needed
//	go run ./cmd/gen-ci-reference -output FILE # write to a different file
//	go run ./cmd/gen-ci-reference -source FILE # read from a different workflow
package main

import (
	"bytes"
	"flag"
	"fmt"
	"os"
	"path/filepath"
	"regexp"
	"sort"
	"strings"

	"github.com/sydlexius/stillwater/internal/filesystem"
	"gopkg.in/yaml.v3"
)

const defaultSourcePath = ".github/workflows/ci.yml"
const defaultOutputPath = "docs/_generated/ci-reference.md"

const fileHeader = "<!-- Generated by gen-ci-reference. DO NOT EDIT. Run 'make generate-docs' to regenerate. -->\n\n"

func main() {
	var (
		checkOnly  bool
		outPath    string
		sourcePath string
	)
	flag.BoolVar(&checkOnly, "check", false, "exit non-zero if the ci-reference file needs to be regenerated")
	flag.StringVar(&outPath, "output", defaultOutputPath, "path to the generated Markdown file to write")
	flag.StringVar(&sourcePath, "source", defaultSourcePath, "path to the CI workflow YAML file to read")
	flag.Parse()

	if err := run(sourcePath, outPath, checkOnly); err != nil {
		fmt.Fprintln(os.Stderr, "gen-ci-reference:", err)
		os.Exit(1)
	}
}

func run(sourcePath, outPath string, checkOnly bool) error {
	src, err := os.ReadFile(sourcePath) //nolint:gosec // G304: developer CLI, path is intentionally configurable
	if err != nil {
		return fmt.Errorf("read %s: %w", sourcePath, err)
	}

	wf, err := parseWorkflow(src)
	if err != nil {
		return fmt.Errorf("parse workflow from %s: %w", sourcePath, err)
	}

	rendered := renderDoc(wf)

	var existing []byte
	switch raw, rerr := os.ReadFile(outPath); { //nolint:gosec // G304: developer CLI, path is intentionally configurable
	case rerr == nil:
		existing = raw
	case os.IsNotExist(rerr):
		existing = nil
	default:
		return fmt.Errorf("read %s: %w", outPath, rerr)
	}

	updated := []byte(fileHeader + rendered)

	if checkOnly {
		if !bytes.Equal(existing, updated) {
			return fmt.Errorf("%s is stale; run `make generate-docs` to regenerate", filepath.ToSlash(outPath))
		}
		return nil
	}

	if bytes.Equal(existing, updated) {
		return nil
	}

	if err := os.MkdirAll(filepath.Dir(outPath), 0o750); err != nil {
		return fmt.Errorf("create output directory: %w", err)
	}

	if err := filesystem.WriteFileAtomic(outPath, updated, 0o644); err != nil {
		return fmt.Errorf("write %s: %w", outPath, err)
	}
	return nil
}

// ciJob holds the parsed representation of one GitHub Actions job.
type ciJob struct {
	ID       string
	Name     string
	Needs    []string
	If       string
	RunsOn   string
	Shards   []string
	ShardMap map[string]string
}

// ciWorkflow is the parsed CI workflow.
type ciWorkflow struct {
	// Jobs in sorted order for deterministic output.
	Jobs []ciJob
}

// workflowYAML is the raw YAML structure for the top-level workflow.
type workflowYAML struct {
	Jobs map[string]jobYAML `yaml:"jobs"`
}

type jobYAML struct {
	Name     string        `yaml:"name"`
	Needs    interface{}   `yaml:"needs"` // string or []string
	If       string        `yaml:"if"`
	RunsOn   string        `yaml:"runs-on"`
	Strategy *strategyYAML `yaml:"strategy"`
	Steps    []struct {
		Name string `yaml:"name"`
		Run  string `yaml:"run"`
	} `yaml:"steps"`
}

// strategyYAML handles the strategy block. The matrix field may be a literal
// mapping or a GitHub Actions expression string (${{ fromJson(...) }}), so it
// is captured as interface{} and inspected afterwards.
type strategyYAML struct {
	Matrix interface{} `yaml:"matrix"` // map[string]interface{} or expression string
}

// shards returns the literal shard list from the matrix, or nil if the matrix
// is a dynamic GitHub Actions expression or does not contain a shard key.
func (s *strategyYAML) shards() []string {
	if s == nil {
		return nil
	}
	m, ok := s.Matrix.(map[string]interface{})
	if !ok {
		return nil
	}
	raw, ok := m["shard"]
	if !ok {
		return nil
	}
	items, ok := raw.([]interface{})
	if !ok {
		return nil
	}
	out := make([]string, 0, len(items))
	for _, it := range items {
		if sv, ok := it.(string); ok {
			out = append(out, sv)
		}
	}
	return out
}

// parseWorkflow parses a GitHub Actions workflow YAML and extracts job
// metadata including dependency graphs and test shard configuration.
func parseWorkflow(src []byte) (*ciWorkflow, error) {
	var raw workflowYAML
	if err := yaml.Unmarshal(src, &raw); err != nil {
		return nil, fmt.Errorf("unmarshal yaml: %w", err)
	}
	if len(raw.Jobs) == 0 {
		return nil, fmt.Errorf("no jobs found in workflow")
	}

	// Sort job IDs for deterministic output.
	ids := make([]string, 0, len(raw.Jobs))
	for id := range raw.Jobs {
		ids = append(ids, id)
	}
	sort.Strings(ids)

	jobs := make([]ciJob, 0, len(ids))
	for _, id := range ids {
		j := raw.Jobs[id]
		job := ciJob{
			ID:     id,
			Name:   j.Name,
			If:     j.If,
			RunsOn: j.RunsOn,
		}
		// `needs` can be a string or a sequence.
		switch v := j.Needs.(type) {
		case string:
			job.Needs = []string{v}
		case []interface{}:
			for _, n := range v {
				if s, ok := n.(string); ok {
					job.Needs = append(job.Needs, s)
				}
			}
		}
		// Extract matrix shards if present and literal (skips dynamic expressions).
		job.Shards = j.Strategy.shards()
		// Extract SHARDS bash map from steps.
		for _, step := range j.Steps {
			if m := extractShardsMap(step.Run); len(m) > 0 {
				job.ShardMap = m
				break
			}
		}
		jobs = append(jobs, job)
	}

	return &ciWorkflow{Jobs: jobs}, nil
}

// shardsEntryRe matches lines like:    [api]="internal/api"
var shardsEntryRe = regexp.MustCompile(`\[([^\]]+)\]="([^"]*)"`)

// extractShardsMap finds a bash SHARDS associative array in a run: script and
// returns the parsed key->value map, or nil if no SHARDS declaration is found.
func extractShardsMap(script string) map[string]string {
	if !strings.Contains(script, "declare -A SHARDS") {
		return nil
	}
	m := make(map[string]string)
	for _, match := range shardsEntryRe.FindAllStringSubmatch(script, -1) {
		m[match[1]] = match[2]
	}
	return m
}

// renderDoc produces the complete Markdown document for the CI reference page.
func renderDoc(wf *ciWorkflow) string {
	var b strings.Builder

	b.WriteString("# CI Reference\n\n")
	b.WriteString("This page documents the structure of the Stillwater CI pipeline. ")
	b.WriteString("It is generated automatically from `.github/workflows/ci.yml` by `cmd/gen-ci-reference` ")
	b.WriteString("and reflects the current job dependency graph and test matrix configuration.\n\n")

	b.WriteString(renderMermaid(wf))
	b.WriteString("\n")
	b.WriteString(renderShardSection(wf))
	b.WriteString("\n")
	b.WriteString(renderAggregatorSection())

	return b.String()
}

// renderMermaid generates a Mermaid flowchart of the CI job dependency graph.
func renderMermaid(wf *ciWorkflow) string {
	var b strings.Builder
	b.WriteString("## Job Dependency Graph\n\n")
	b.WriteString("```mermaid\n")
	b.WriteString("flowchart TD\n")

	// Emit nodes with labels.
	for _, job := range wf.Jobs {
		label := job.Name
		if label == "" {
			label = job.ID
		}
		// Sanitize label: Mermaid node IDs cannot contain hyphens in bare form,
		// so quote the label content in square brackets. Use the job ID (no
		// special chars after sanitization) as the node ID.
		nodeID := sanitizeID(job.ID)
		fmt.Fprintf(&b, "    %s[\"%s\"]\n", nodeID, escapeQuotes(label))
	}

	b.WriteString("\n")

	// Emit edges from needs relationships.
	for _, job := range wf.Jobs {
		nodeID := sanitizeID(job.ID)
		for _, need := range job.Needs {
			needID := sanitizeID(need)
			fmt.Fprintf(&b, "    %s --> %s\n", needID, nodeID)
		}
	}

	b.WriteString("```\n")
	return b.String()
}

// sanitizeID replaces characters that are invalid in Mermaid node IDs.
func sanitizeID(id string) string {
	return strings.NewReplacer("-", "_").Replace(id)
}

// escapeQuotes escapes double quotes for use inside Mermaid node labels.
func escapeQuotes(s string) string {
	return strings.ReplaceAll(s, `"`, `'`)
}

// renderShardSection generates the test shard table section.
func renderShardSection(wf *ciWorkflow) string {
	var testJob *ciJob
	for i := range wf.Jobs {
		if wf.Jobs[i].ID == "test" {
			testJob = &wf.Jobs[i]
			break
		}
	}

	var b strings.Builder
	b.WriteString("## Test Matrix Shards\n\n")

	if testJob == nil || len(testJob.Shards) == 0 {
		b.WriteString("No test shards configured.\n")
		return b.String()
	}

	b.WriteString("| Shard | Packages / Notes | Partition |\n")
	b.WriteString("|---|---|---|\n")

	shardMap := testJob.ShardMap

	for _, shard := range testJob.Shards {
		var pkgs, partition string
		switch {
		case shard == "rest":
			pkgs = "_dynamic_"
			partition = "Remainder: all packages not covered by a named shard, derived at runtime to auto-include new packages"
		case strings.HasSuffix(shard, "-1") || strings.HasSuffix(shard, "-2"):
			base := shard[:len(shard)-2]
			idx := shard[len(shard)-1:]
			if v, ok := shardMap[base]; ok {
				pkgs = v
			} else {
				pkgs = "./" + base + "/..."
			}
			partition = fmt.Sprintf("Round-robin by index parity (bucket %s of 2): test functions at sorted position ≡ %s mod 2", idx, string(rune('0'+idx[0]-'1')))
		default:
			if v, ok := shardMap[shard]; ok {
				pkgs = v
			} else {
				pkgs = "./" + shard + "/..."
			}
			partition = "Named shard: all tests in listed packages"
		}
		fmt.Fprintf(&b, "| `%s` | `%s` | %s |\n", shard, pkgs, partition)
	}

	return b.String()
}

// renderAggregatorSection generates the prose section explaining the
// test-summary aggregator job pattern.
func renderAggregatorSection() string {
	return `## Test Aggregator Pattern

The ` + "`test-summary`" + ` job owns the **"Test"** check name that branch protection
requires. It runs under ` + "`always()`" + ` so a check result is always posted even
when individual shards fail or are canceled.

This pattern decouples the matrix shard count from branch protection rules:
shards can be added, removed, or renamed without updating the required-checks
list. Branch protection requires one check ("Test") and ` + "`test-summary`" + `
always reports it -- passing only when every shard in the matrix succeeds.
`
}
