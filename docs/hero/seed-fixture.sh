#!/usr/bin/env bash
#
# seed-fixture.sh - deterministically rebuild the #1756 hero fixture FROM SCRATCH
# and start the capture server ready for docs/hero/nav-clips.mjs.
#
# The fixture DB is NOT a deliverable and is never committed (it also would carry
# encrypted secrets). This script is the regenerable path: a clean checkout can
# run  seed-fixture.sh -> nav-clips.mjs -> build-clips.mjs -> remotion render  and reproduce the hero.
#
# WHAT IT BUILDS
#   * fixture.db from the committed, sanitized SQL seed (docs/hero/seed/fixture.sql)
# - 12 public-domain composers, example Emby/Jellyfin/Lidarr connections, and
#     provider priorities. The seed contains NO real secrets (provider API keys
#     dropped, connection tokens blanked).
#   * Artist artwork from the committed PD portraits (docs/hero/portraits/), copied
#     into the fixture library and repointed to a machine-local path.
#   * A throwaway encryption key, auto-generated by the app on first boot next to
#     the DB (nothing in the seed is encrypted with a fixed key).
#   * MASKED dummy provider keys, set via the app's own settings API (so they are
#     validly encrypted with this run's throwaway key) - the fixture looks
#     "providers configured" without any real secret, and route-mock intercepts
#     every provider call regardless.
#
# USAGE
#   docs/hero/seed-fixture.sh                 # build + start server on :1991
#   HERO_PORT=1991 HERO_DIR=/tmp/hero-1756 docs/hero/seed-fixture.sh
#
# Stop the server later with ONLY your own listener:
#   lsof -ti:$HERO_PORT -sTCP:LISTEN | xargs kill
set -euo pipefail

REPO="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
HERO_DIR="${HERO_DIR:-/tmp/hero-1756}"
PORT="${HERO_PORT:-1991}"
FIXTURE="$HERO_DIR/fixture"
LIB="$FIXTURE/library"
DB="$FIXTURE/fixture.db"
BIN="$HERO_DIR/sw"
SEED_SQL="$REPO/docs/hero/seed/fixture.sql"
PORTRAITS="$REPO/docs/hero/portraits"
ADMIN_USER="herofixture-admin"; ADMIN_PASS="herofixture-pw"

command -v sqlite3 >/dev/null || { echo "FATAL: sqlite3 not found" >&2; exit 1; }
[ -f "$SEED_SQL" ] || { echo "FATAL: seed SQL missing: $SEED_SQL" >&2; exit 1; }
[ -d "$PORTRAITS" ] || { echo "FATAL: committed portraits missing: $PORTRAITS (run fetch-portraits.sh)" >&2; exit 1; }

mkdir -p "$FIXTURE" "$LIB"

# 1. Build the binary if needed (native only; never docker).
if [ ! -x "$BIN" ]; then
  echo "building stillwater -> $BIN"
  ( cd "$REPO" && go build -o "$BIN" ./cmd/stillwater )
fi

# 2. Fresh DB from the sanitized seed.
rm -f "$DB" "$DB-wal" "$DB-shm"
sqlite3 "$DB" < "$SEED_SQL"

# 3. Copy committed PD portraits into the library and repoint artists at a
#    machine-local path (the seed's stored paths are placeholders).
python3 - "$DB" "$LIB" "$PORTRAITS" <<'PY'
import sys, sqlite3, os, shutil, glob, uuid
db, lib, portraits = sys.argv[1], sys.argv[2], sys.argv[3]
con = sqlite3.connect(db); cur = con.cursor()
def ensure_thumb(aid):
    if cur.execute("SELECT 1 FROM artist_images WHERE artist_id=? AND image_type='thumb' AND slot_index=0", (aid,)).fetchone():
        cur.execute("UPDATE artist_images SET exists_flag=1 WHERE artist_id=? AND image_type='thumb' AND slot_index=0", (aid,))
    else:
        cur.execute("INSERT INTO artist_images (id, artist_id, image_type, slot_index, exists_flag) VALUES (?,?,?,?,1)",
                    (str(uuid.uuid4()), aid, 'thumb', 0))
for src in sorted(glob.glob(os.path.join(portraits, '*', 'folder.jpg'))):
    name = os.path.basename(os.path.dirname(src))
    dst_dir = os.path.join(lib, name); os.makedirs(dst_dir, exist_ok=True)
    shutil.copy2(src, os.path.join(dst_dir, 'folder.jpg'))
    row = cur.execute("SELECT id FROM artists WHERE name=?", (name,)).fetchone()
    if row:
        cur.execute("UPDATE artists SET path=?, is_classical=1 WHERE id=?", (dst_dir, row[0]))
        ensure_thumb(row[0])
        print(f"  artwork {name}")
    else:
        print(f"  WARN: portrait '{name}' has no matching artist row")
# Repoint the filesystem libraries away from any container/real path.
cur.execute("UPDATE libraries SET path=? WHERE name IN ('Music','Classical')", (lib,))
con.commit(); con.close()
PY

# 4. Start the capture server (self-generates its throwaway encryption.key next
#    to the DB). Kill only a prior listener on this port first.
lsof -ti:"$PORT" -sTCP:LISTEN 2>/dev/null | xargs kill 2>/dev/null || true
echo "starting server on :$PORT"
# nohup + disown so the capture server outlives this script's shell.
SW_DB_PATH="$DB" SW_PORT="$PORT" SW_UX=dual SW_MUSIC_PATH="$LIB" \
  nohup "$BIN" > "$HERO_DIR/server.log" 2>&1 &
SRV=$!
disown "$SRV" 2>/dev/null || true

# Wait for health.
for i in $(seq 1 40); do
  if curl -sf "http://127.0.0.1:$PORT/api/v1/health" >/dev/null 2>&1; then break; fi
  sleep 0.5
  [ "$i" = 40 ] && { echo "FATAL: server did not become healthy; see $HERO_DIR/server.log" >&2; exit 1; }
done
echo "server healthy (pid $SRV)"

# 5. Mask provider keys via the app's settings API (validly encrypted dummies).
CJ="$HERO_DIR/_seed_cj"; curl -s -c "$CJ" "http://127.0.0.1:$PORT/api/v1/health" >/dev/null
CSRF="$(grep csrf_token "$CJ" | awk '{print $7}')"
LOGIN_STATUS="$(curl -s -o /dev/null -w '%{http_code}' -b "$CJ" -c "$CJ" -X POST "http://127.0.0.1:$PORT/api/v1/auth/login" \
  -H "Content-Type: application/json" -H "X-CSRF-Token: $CSRF" \
  -d "{\"username\":\"$ADMIN_USER\",\"password\":\"$ADMIN_PASS\"}")"
# Make login failure fatal: otherwise every settings PUT below silently 401s and
# the script still reports "Fixture ready" with the keys never actually set.
[ "$LOGIN_STATUS" = "200" ] || { echo "FATAL: fixture login failed (HTTP $LOGIN_STATUS)" >&2; exit 1; }
for kv in \
  "provider.fanarttv.api_key=FAKE-FANARTTV-KEY-000000000000" \
  "provider.audiodb.api_key=FAKE-AUDIODB-KEY-0000000000000" \
  "provider.discogs.api_key=FAKE-DISCOGS-KEY-0000000000000" \
  "provider.genius.api_key=FAKE-GENIUS-KEY-00000000000000" \
  "provider.lastfm.api_key=FAKE-LASTFM-KEY-00000000000000" \
  "provider.spotify.api_key=FAKE-SPOTIFY-KEY-0000000000000" ; do
  k="${kv%%=*}"; v="${kv#*=}"
  curl -s -b "$CJ" -X PUT "http://127.0.0.1:$PORT/api/v1/settings" \
    -H "Content-Type: application/json" -H "X-CSRF-Token: $CSRF" \
    -d "{\"$k\":\"$v\"}" >/dev/null || echo "  WARN: could not set $k" >&2
done
rm -f "$CJ"
echo "masked provider keys set (all fake, encrypted with the throwaway key)"

cat <<EOF

Fixture ready.
  URL   : http://127.0.0.1:$PORT   (SW_UX=dual)
  Login : $ADMIN_USER / $ADMIN_PASS
  DB    : $DB   (gitignored; not a deliverable)
  Stop  : lsof -ti:$PORT -sTCP:LISTEN | xargs kill

Next:
  node docs/hero/mocks/gen-mocks.sh   # refresh route-mock fixtures (optional)
  node docs/hero/nav-clips.mjs        # record the per-screen clips
  cd docs/hero/captions && node build-clips.mjs \\
    && npx remotion render src/index.ts HeroStitched out/hero-stitched.mp4
EOF
