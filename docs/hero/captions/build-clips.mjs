// build-clips.mjs - convert the recorded per-screen webm clips into trimmed mp4s
// and regenerate src/clips.generated.ts. For each clip it:
//   1. trims the blank/load lead (the recorded `contentStart` seconds),
//   2. transcodes webm -> H.264 mp4 (Remotion OffthreadVideo-friendly),
//   3. rebases each caption's atSec by the trim offset (clamped >= 0),
//   4. probes the trimmed file's real duration.
// Idempotent; run AFTER nav-clips.mjs. This replaces the old inline convert block
// (which was lost to a context compaction - keep it as a file so it survives).
import { execFileSync } from 'node:child_process';
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));
const MANIFEST = process.env.HERO_CLIPS_JSON || '/tmp/hero-1756/clips/clips.json';
const PUBLIC = join(HERE, 'public', 'clips');
const GEN = join(HERE, 'src', 'clips.generated.ts');
mkdirSync(PUBLIC, { recursive: true });

// ffmpeg/ffprobe are hard requirements; check up front so a missing binary
// gives a clear prerequisite error rather than a raw execFileSync stack trace.
for (const tool of ['ffmpeg', 'ffprobe']) {
  try {
    execFileSync(tool, ['-version'], { stdio: 'ignore' });
  } catch {
    throw new Error(`required tool not found on PATH: ${tool}`);
  }
}

const clips = JSON.parse(readFileSync(MANIFEST, 'utf8'));
const probe = (f) =>
  parseFloat(
    execFileSync('ffprobe', ['-v', 'error', '-show_entries', 'format=duration', '-of', 'default=nw=1:nk=1', f])
      .toString()
      .trim(),
  );

const out = clips.map((c, i) => {
  const idx = String(i + 1).padStart(2, '0');
  const dest = join(PUBLIC, `${idx}-${c.name}.mp4`);
  const ss = Math.max(0, c.contentStart || 0);
  execFileSync(
    'ffmpeg',
    ['-y', '-ss', String(ss), '-i', c.file, '-an', '-c:v', 'libx264', '-pix_fmt', 'yuv420p', '-crf', '18', '-preset', 'veryfast', '-movflags', '+faststart', dest],
    { stdio: 'ignore' },
  );
  const durSec = +probe(dest).toFixed(3);
  const captions = c.captions.map((cap) => {
    // Guard against a non-numeric atSec: NaN.toFixed() -> "NaN", which
    // JSON.stringify emits as null in the generated TS and breaks the number type.
    const at = Number.isFinite(cap.atSec) ? Math.max(0, cap.atSec - ss) : 0;
    return { group: cap.group, atSec: +at.toFixed(3) };
  });
  console.log(`[build-clips] ${idx}-${c.name}: trim @${ss}s -> ${durSec}s, ${captions.length} caption group(s)`);
  return { name: c.name, src: `clips/${idx}-${c.name}.mp4`, durSec, captions };
});

const body = `// AUTO-GENERATED by build-clips.mjs - do not edit.
export type ClipCaption = { group: string; atSec: number };
export type Clip = { name: string; src: string; durSec: number; captions: ClipCaption[] };
export const CLIPS: Clip[] = ${JSON.stringify(out, null, 2)};
`;
writeFileSync(GEN, body);
console.log(`[build-clips] wrote ${GEN}`);
